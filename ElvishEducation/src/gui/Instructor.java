    private void practiceCheckActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_practiceCheckActionPerformed
    {//GEN-HEADEREND:event_practiceCheckActionPerformed
        // TODO add your handling code here:
        if (!incorrect.toString().equals(""))
        {
            if (input.getText().equalsIgnoreCase(promptAndAnswer[1]))
            {
                incorrect.setForeground( new Color( 0xCDFFFF ) );
                incorrect.setText( "Correct!" );

                setupPractice();
            } else
            {
                incorrect.setForeground(Color.red);
                incorrect.setText(promptAndAnswer[1]);
            }
        } else
        {
            setupPractice();
        }
    }//GEN-LAST:event_practiceCheckActionPerformed

